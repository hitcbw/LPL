def _download(url: str, root: str):
    os.makedirs(root, exist_ok=True)  # 创建目标目录（如果不存在），`exist_ok=True` 表示如果目录已存在则不会引发错误
    filename = os.path.basename(url)

    expected_sha256 = url.split("/")[-2] # 从URL中提取预期的SHA256校验和
    download_target = os.path.join(root, filename) # 下载文件的目标路径

    # 如果下载目标已存在且不是常规文件，引发运行时错误
    if os.path.exists(download_target) and not os.path.isfile(download_target):
        raise RuntimeError(f"{download_target} exists and is not a regular file")

    # 如果下载目标已存在且是常规文件，检查其SHA256校验和是否与预期值匹配
    if os.path.isfile(download_target):
        if hashlib.sha256(open(download_target, "rb").read()).hexdigest() == expected_sha256:
            return download_target # 返回下载的文件路径
        else: # 如果SHA256校验和不匹配，发出警告并重新下载文件
            warnings.warn(f"{download_target} exists, but the SHA256 checksum does not match; re-downloading the file")

    # 使用urllib.request.urlopen打开URL，并用open打开下载目标以进行写操作
    with urllib.request.urlopen(url) as source, open(download_target, "wb") as output:
        with tqdm(total=int(source.info().get("Content-Length")), ncols=80, unit='iB', unit_scale=True, unit_divisor=1024) as loop:
            while True:
                buffer = source.read(8192)
                if not buffer:
                    break

                output.write(buffer)
                loop.update(len(buffer))

    # 下载完成后，再次检查下载的文件的SHA256校验和是否与预期值匹配
    if hashlib.sha256(open(download_target, "rb").read()).hexdigest() != expected_sha256:
        raise RuntimeError(f"Model has been downloaded but the SHA256 checksum does not not match")

    return download_target # 返回下载的文件路径


def _convert_image_to_rgb(image):
    return image.convert("RGB")


def _transform(n_px):
    return Compose([ # 使用 Compose 创建一个图像转换操作的序列
        Resize(n_px, interpolation=BICUBIC), # 将图像大小调整为 n_px，使用 BICUBIC 插值方法
        CenterCrop(n_px), # 对图像进行中心裁剪，保留中央区域，裁剪后大小为 n_px
        _convert_image_to_rgb, # 将图像转换为 RGB 格式
        ToTensor(), # 将图像转换为 PyTorch 的 Tensor 格式
        Normalize((0.48145466, 0.4578275, 0.40821073), (0.26862954, 0.26130258, 0.27577711)), # 对图像进行标准化操作，使用给定的均值和标准差
    ])


